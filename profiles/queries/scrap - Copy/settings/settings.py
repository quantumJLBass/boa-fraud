class VPNConfigList:
    """
    A class to manage VPN configuration file paths with a base directory.

    Attributes:
    ----------
    base_path : str
        The base directory path where the VPN configuration files are located.
    config_files : list
        A list of VPN configuration file names.

    Methods:
    -------
    __getitem__(index):
        Returns the full path to the VPN configuration file at the specified index.
    """
    def __init__(self, base_path, config_files):
        """
        Initializes the VPNConfigList with a base directory path and a list of configuration file names.

        Parameters:
        ----------
        base_path : str
            The base directory path where the VPN configuration files are located.
        config_files : list
            A list of VPN configuration file names.
        """
        self.base_path = base_path
        self.config_files = config_files

    def __getitem__(self, index):
        """
        Returns the full path to the VPN configuration file at the specified index by 
        concatenating the base path with the file name.

        Parameters:
        ----------
        index : int
            The index of the VPN configuration file in the config_files list.

        Returns:
        -------
        str
            The full path to the VPN configuration file.

        Raises:
        ------
        IndexError:
            If the index is out of range of the config_files list.
        """
        try:
            # Return the full path by concatenating base path and file name
            return self.base_path + self.config_files[index]
        except IndexError:
            raise IndexError(f"Index {index} out of range. Please provide a valid index.")

